// DOM elementlerini seç
const languageSelect = document.getElementById('languageSelect');
const hamburgerMenu = document.getElementById('hamburgerMenu');
const navMenu = document.getElementById('navMenu');

// Dil değiştirme fonksiyonu
languageSelect.addEventListener('change', (e) => {
  const lang = e.target.value;
  Object.keys(translations[lang]).forEach(id => {
    const element = document.getElementById(id);
    if (element) {
      element.textContent = translations[lang][id];
    }
  });
});

// Hamburger menü fonksiyonalitesi
hamburgerMenu.addEventListener('click', () => {
  hamburgerMenu.classList.toggle('active');
  navMenu.classList.toggle('active');
});

// Menü linklerine tıklandığında menüyü kapat
const navLinks = navMenu.querySelectorAll('a');
navLinks.forEach(link => {
  link.addEventListener('click', () => {
    hamburgerMenu.classList.remove('active');
    navMenu.classList.remove('active');
  });
});

// Sayfa dışına tıklandığında menüyü kapat
document.addEventListener('click', (e) => {
  if (!hamburgerMenu.contains(e.target) && !navMenu.contains(e.target)) {
    hamburgerMenu.classList.remove('active');
    navMenu.classList.remove('active');
  }
});

// Ekran boyutu değiştiğinde menüyü sıfırla
window.addEventListener('resize', () => {
  if (window.innerWidth > 768) {
    hamburgerMenu.classList.remove('active');
    navMenu.classList.remove('active');
  }
});

// Form işleme fonksiyonları
const contactForm = document.querySelector('.contact-form');

if (contactForm) {
  contactForm.addEventListener('submit', async (e) => {
    e.preventDefault();
    
    const submitBtn = contactForm.querySelector('.submit-btn');
    const originalText = submitBtn.textContent;
    
    // Submit butonunu devre dışı bırak
    submitBtn.disabled = true;
    submitBtn.textContent = 'Gönderiliyor...';
    
    // Mevcut mesajları temizle
    const existingMessage = contactForm.querySelector('.form-message');
    if (existingMessage) {
      existingMessage.remove();
    }
    
    try {
      const formData = new FormData(contactForm);
      
      // Formspree endpoint'i (bu kısmı değiştirmeniz gerekecek)
      const response = await fetch(contactForm.action, {
        method: 'POST',
        body: formData,
        headers: {
          'Accept': 'application/json'
        }
      });
      
      if (response.ok) {
        showFormMessage('Başvurunuz başarıyla gönderildi! En kısa sürede size dönüş yapacağız.', 'success');
        contactForm.reset();
      } else {
        throw new Error('Form gönderilemedi');
      }
    } catch (error) {
      console.error('Form gönderme hatası:', error);
      showFormMessage('Başvuru gönderilirken bir hata oluştu. Lütfen tekrar deneyin veya info@appanddesign.com adresine e-posta gönderin.', 'error');
    } finally {
      // Submit butonunu tekrar aktif et
      submitBtn.disabled = false;
      submitBtn.textContent = originalText;
    }
  });
}

// Form mesajı gösterme fonksiyonu
function showFormMessage(message, type) {
  const messageDiv = document.createElement('div');
  messageDiv.className = `form-message ${type}`;
  messageDiv.textContent = message;
  
  const submitBtn = contactForm.querySelector('.submit-btn');
  contactForm.insertBefore(messageDiv, submitBtn);
  
  // 5 saniye sonra mesajı kaldır
  setTimeout(() => {
    if (messageDiv.parentNode) {
      messageDiv.remove();
    }
  }, 5000);
}

// Smooth scroll için
document.querySelectorAll('a[href^="#"]').forEach(anchor => {
  anchor.addEventListener('click', function (e) {
    e.preventDefault();
    const targetId = this.getAttribute('href');
    const target = document.querySelector(targetId);
    
    if (target) {
      // Header'ın yüksekliğini hesapla
      const headerHeight = document.querySelector('header').offsetHeight;
      const targetPosition = target.offsetTop - headerHeight - 20; // 20px ekstra boşluk
      
      window.scrollTo({
        top: targetPosition,
        behavior: 'smooth'
      });
    }
  });
});

// Sayfa yüklendiğinde URL'deki hash'i kontrol et
window.addEventListener('load', () => {
  if (window.location.hash) {
    const target = document.querySelector(window.location.hash);
    if (target) {
      const headerHeight = document.querySelector('header').offsetHeight;
      const targetPosition = target.offsetTop - headerHeight - 20;
      
      setTimeout(() => {
        window.scrollTo({
          top: targetPosition,
          behavior: 'smooth'
        });
      }, 100);
    }
  }
}); 